a = int(input("Enter a number: " ))
b = int(input("Enter b number: " ))


# if 
if a > 90:
    print("a is greater than b")



# if else 
if a < 60:
    print("b is greater than a")    
else:
    print("a is equal to b")


# if elif else
if a > b:
    print("a is greater than b")
elif a < b:
    print("b is greater than a")
elif a == b:
    print("a is equal to b")
else:
    print("Invalid Input")